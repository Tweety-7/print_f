#include "libft.h"

void	ft_putnbr_fd(int nb, int fd)
{
	int	i;
	int	a[11];

	i = 0;
	if (nb == -2147483648)
	{
		a[i++] = 8;
		nb = -214748364;
	}
	if (nb < 0)
	{
		ft_putchar_fd('-', fd);
		nb = -nb;
	}
	while (nb / 10 > 0)
	{
		a[i++] = nb % 10;
		nb = (nb - (nb % 10)) / 10;
	}
	a[i] = nb;
	while (i > -1)
	{
		ft_putchar_fd('0' + a[i], fd);
		i = i - 1;
	}
}
